object ItemEditDestination : NavigationDestination {
    override val route = "item_edit"
    override val titleRes = R.string.edit_item_title
    const val itemIdArg = "itemId"
    val routeWithArgs = "$route/{$itemIdArg}"
}

@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun ItemEditScreen(
    navigateBack: () -> Unit,
    onNavigateUp: () -> Unit,
    modifier: Modifier = Modifier,
    viewModel: ItemEditViewModel = viewModel(factory = AppViewModelProvider.Factory)
) {
    val coroutineScope = rememberCoroutineScope()
    Scaffold(
        topBar = {
            InventoryTopAppBar(
                title = stringResource(ItemEditDestination.titleRes),
                canNavigateBack = true,
                navigateUp = onNavigateUp
            )
        },
        modifier = modifier
    ) { innerPadding ->
        ItemEntryBody(
            itemUiState = viewModel.itemUiState,
            onItemValueChange = viewModel::updateUiState, // OR: { viewModel.updateUiState(it) } *
            onSaveClick = {
                coroutineScope.launch {
                    viewModel.updateItem()
                    navigateBack()
                }
            },
            modifier = Modifier
                .padding(
                    start = innerPadding.calculateStartPadding(LocalLayoutDirection.current),
                    end = innerPadding.calculateEndPadding(LocalLayoutDirection.current),
                    top = innerPadding.calculateTopPadding()
                )
                .verticalScroll(rememberScrollState())
        )
        /* What `viewModel::updateUiState` means

        :: is Kotlin’s callable-reference operator.
        • viewModel::updateUiState doesn’t call the function.
        • It creates a reference a value of type (ItemDetails) -> Unit that can be stored or passed around.
        Somewhere else typically in a Composable that reference is passed to a parameter that expects a
        function taking an ItemDetails and returning Unit

        Why isn’t there an `itemDetails` argument here?

        Because the function isn’t invoked at this point—only its reference is handed off.
        The argument will be supplied later by the code that calls the callback e.g., when the user
        edits a field. Think of it like handing someone a phone number; the actual conversation
        the call with arguments happens later.
        */
    }
}

